#include "OpenDIVA/Gui.hpp"
#include <QDebug>
#include <QKeyEvent>
void Gui::keyPressEvent(QKeyEvent *event) {
    switch (event->key()) {
    case Qt::Key_Left:
        break;
    case Qt::Key_Right:
        break;
    case Qt::Key_Up:
        break;
    case Qt::Key_Down:
        break;
    }
}
